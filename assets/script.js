const STORAGE_KEY = "site_language";
const DEFAULT_LANG = "sk";

const getValueByPath = (object, path) =>
  path.split(".").reduce((acc, segment) => (acc ? acc[segment] : undefined), object);

const applyLanguage = (lang) => {
  const locale = translations[lang] ? lang : DEFAULT_LANG;
  const copy = translations[locale];

  document.documentElement.lang = locale;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const value = getValueByPath(copy, key);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    const value = getValueByPath(copy, key);
    if (typeof value === "string") {
      element.setAttribute("placeholder", value);
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.getAttribute("data-i18n-alt");
    const value = getValueByPath(copy, key);
    if (typeof value === "string") {
      element.setAttribute("alt", value);
    }
  });

  if (copy.meta?.title) {
    document.title = copy.meta.title;
  }

  const description = document.querySelector('meta[name="description"]');
  if (description && copy.meta?.description) {
    description.setAttribute("content", copy.meta.description);
  }

  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle && copy.meta?.title) {
    ogTitle.setAttribute("content", copy.meta.title);
  }

  const ogDescription = document.querySelector('meta[property="og:description"]');
  if (ogDescription && copy.meta?.description) {
    ogDescription.setAttribute("content", copy.meta.description);
  }

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === locale);
  });

  try {
    localStorage.setItem(STORAGE_KEY, locale);
  } catch (error) {
    // Ignore storage errors in restricted environments.
  }
};

const initReveal = () => {
  const sections = document.querySelectorAll(".fade-section");
  if (!("IntersectionObserver" in window)) {
    sections.forEach((section) => section.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries, currentObserver) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          currentObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.2 }
  );

  sections.forEach((section) => observer.observe(section));
};

document.addEventListener("DOMContentLoaded", () => {
  const yearElement = document.getElementById("year");
  if (yearElement) {
    yearElement.textContent = String(new Date().getFullYear());
  }

  const savedLanguage = (() => {
    try {
      return localStorage.getItem(STORAGE_KEY);
    } catch (error) {
      return null;
    }
  })();

  applyLanguage(savedLanguage || DEFAULT_LANG);

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.lang));
  });

  initReveal();
});
